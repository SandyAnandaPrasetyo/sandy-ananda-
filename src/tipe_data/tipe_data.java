public class tipe_data {
 
    public static void main(String[] args) {
        
        System.out.println("--------------------------------");
        System.out.println("MENCOBA TIPE DATA");
        System.out.println("--------------------------------");
        System.out.println("MENGGUNAKAN STRING,FLOAT,CHAR,DOUBLE");
        System.out.println("--------------------------------");
        System.out.println("      BY SANDXTEAM       ");
        System.out.println("--------------------------------");
        //mendeklarasikan variabel dengan tipe data string
        
        String nama = "sandy ananada p";
         String nim = "2355201039";
        // Mendeklarasikan variabel dengan tipe data int
      
        
        int umur = 25;
        
        // Mendeklarasikan variabel dengan tipe data float
        float tinggi = 175.5f; // Harus ditambahkan 'f' di belakang nilai float
        
        // Mendeklarasikan variabel dengan tipe data char
        char jenisKelamin = 'L';
        
        // Mendeklarasikan variabel dengan tipe data double
        double berat = 68.75;

        // Menampilkan nilai variabel ke layar
        System.out.println("nama"+nama);
        System.out.println("nim"+nim);
        System.out.println("Umur: " + umur);
        System.out.println("Tinggi: " + tinggi);
        System.out.println("Jenis Kelamin: " + jenisKelamin);
        System.out.println("Berat: " + berat);
    }
}

    

